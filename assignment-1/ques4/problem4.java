import java.util.*;
class Operations{
	public static void main(String[] args){
		System.out.println("Result of 10&50 :");
		System.out.println(10&50);
		System.out.println("Result of 30||40 :");
		System.out.println(30|40);
		System.out.println("Result of ~50 :");
		System.out.println(~50);
		System.out.println("Result of 60>>3 :");
		System.out.println(60>>3);
		System.out.println("Result of 70<<8 :");
		System.out.println(70<<8);
		System.out.println("Result of 55>>>2 :");
		System.out.println(55>>>2);
	}
}
